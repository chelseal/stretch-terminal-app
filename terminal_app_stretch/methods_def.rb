# Upper body stretches grouping
Shoulders_stretch = "Raise both shoulders at once up toward the ears. Drop them and repeat 10 times each direction."

Wrist_forearm_stretch = "Straighten your arm, holding your fingers, gently bend wrist up until you feel a stretch in the forearm. Hold for 15 seconds then release. Holding your fingers, gently bend wrist down until you feel a stretch in the forearm, hold for 15 seconds then release. Repeat with your other arm."

Upper_trap_stretch = "Gently pull your head toward each shoulder until a light stretch is felt. Hold the pose for 10 to 15 seconds. Alternate once on each side."

Neck_stretch = "Relax and lean your head forward. Slowly roll toward one side and hold for 10 seconds. Repeat on other side. Relax again and lift your chin back to starting position. Do this three times for each direction."

